# Identity : is , is not
# is returns True if both variables are the same object
# is not returns the True if both variables are not the same object
x = [1, 2, 3]
y = [1, 2, 3]
print(x is y)  # OP : False since both are storing in different area
print(x is not y)  # OP : True

a = 10
print(a)
print(-a) # Unary operator
print(~a) # Bitwise operator

