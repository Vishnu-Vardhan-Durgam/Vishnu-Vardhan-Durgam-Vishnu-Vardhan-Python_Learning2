# Assignment : =, +=, -= ,/= ,%= ,**= ,//=
#   = Assignment
# + = add and assign
# - = subtract and assign
# * = multiply and assign
# / = divide and assign
# % = modulus and assign
# ** = exponential and assign
# // = floor divide and assign
a = 5
a += 2  # a = a+5 increased by 2
print(a)

a -= 1  # a = a-1
print(a)
a *= 3  # a = a*3
print(a)
a /= 2  # a = a/2
print(a)
a %= 4  # a = %= 4
print(a)
