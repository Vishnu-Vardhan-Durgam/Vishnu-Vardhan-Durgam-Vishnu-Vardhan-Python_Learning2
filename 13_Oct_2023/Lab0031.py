# Arithmetic operators :  +, - ,*, %, /, // ,**
a = int(input("Enter a value of a\n"))  # 10 (Hard coded value)
b = int(input("Enter a value of b\n"))  # 3 (Hard coded value)

print(a + b)  # addition OP:13
print(a - b)  # subtraction OP: 7
print(a * b)  # multiplication OP: 30
print(a / b)  # division OP: 3.3333
print(a ** b)  # exponentiation OP: 1000 # 10^3
print(a // b)  # floor division (quotient) OP: 3 #10//3 --> 3.33 (Returns 3)
print(a % b)  # modulus (reminder) OP: 1
