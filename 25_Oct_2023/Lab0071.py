squares = [1, 4, 9, 16, 25, 33, 44, 56, 68, 74, 85, 99]

print(squares[0])
print(len(squares))
print(squares.count(3))  # Return number of occurrences of value
print(squares[-1])  # OP = 25
print(squares[1:3])  # OP = [4, 9]
print(squares[3:7:2])  # OP = [16, 33]
print(squares[:7:2])  # OP = [1, 9, 25, 44]
print(squares[-4:-9:-2])  # OP = [68, 44, 25] ----> reverse


